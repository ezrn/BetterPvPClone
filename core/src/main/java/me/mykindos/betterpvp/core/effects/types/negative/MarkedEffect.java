@BPvPListener
@Singleton
public class MarkedEffect extends VanillaEffectType implements Listener {

    private final double baseExtraDamage = 3.0;
    private final double extraDamageIncreasePerLevel = 3.0;

    @Override
    public String getName() {
        return "Marked";
    }

    @Override
    public boolean isNegative() {
        return true;
    }

    @Override
    public PotionEffectType getVanillaPotionType() {
        return PotionEffectType.UNLUCK;
    }

    @Override
    public String getDescription(int level) {
        return "<white>Marked " + UtilFormat.getRomanNumeral(level) + " <reset>increases the first instance of damage taken by <val>" + (baseExtraDamage + (extraDamageIncreasePerLevel * level)) + "</val>";
    }
}
